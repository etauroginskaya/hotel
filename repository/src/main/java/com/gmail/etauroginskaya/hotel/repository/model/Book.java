package com.gmail.etauroginskaya.hotel.repository.model;

public class Book {
    private String day;
    private String roomID;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getRoomID() {
        return roomID;
    }

    public void setRoomID(String roomID) {
        this.roomID = roomID;
    }
}